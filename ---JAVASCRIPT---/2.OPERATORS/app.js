// ARTHIMETIC OPERATORS--

let a = 20;
let b = 10;
console.log( "Sum of a and b is :" ,a + b);
console.log( "Sub of a and b is :" ,a - b);
console.log( "Mul of a and b is :" ,a * b);
console.log( "Div of a and b is :" ,a / b);
console.log( "Mod of a and b is :" ,a % b);
console.log( "Power of a and b is :",a ** b);

// UNARY OPERATOR--

let d= 10;

// post-increment.
console.log(d++);  //ans 20.( firstly change the value then assign.)

//pre-increment.
console.log(++d);  // ans 22.(firstly assign the pervious value then add the new value.)

// post-decrement.
console.log(d--);

// pre-decrement.
console.log(--d);

// ASSIGNMNET OPERATOR--

let c= 10;
console.log(a = c+1);  //add.
console.log(a = c-1);  //sub.


